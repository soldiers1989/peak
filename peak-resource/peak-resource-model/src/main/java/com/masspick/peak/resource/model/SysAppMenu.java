package com.masspick.peak.resource.model;

/**
 * SysAppMenu
 */
public class SysAppMenu {
    /**
     * id
     */
    private String id;

    /**
     * appId
     */
    private String appId;

    /**
     * menuId
     */
    private String menuId;

    /**
     * @return String
     */
    public String getId() {
        return id;
    }

    /**
     * @param id
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return String
     */
    public String getAppId() {
        return appId;
    }

    /**
     * @param appId
     */
    public void setAppId(String appId) {
        this.appId = appId;
    }

    /**
     * @return String
     */
    public String getMenuId() {
        return menuId;
    }

    /**
     * @param menuId
     */
    public void setMenuId(String menuId) {
        this.menuId = menuId;
    }
}
